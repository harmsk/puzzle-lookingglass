package edu.wustl.lookingglass.ide.community.connection;

import org.lgna.croquet.Operation;
import org.lgna.croquet.views.MigPanel;

/**
 * @author Caitlin Kelleher
 */
public class OneLineAnonymousAccessCard extends OneLineRefreshableAccessCard {

	public OneLineAnonymousAccessCard( Operation refreshOperation ) {
		super( java.util.UUID.fromString( "faa091cb-89fa-4808-b6e3-0897f070643c" ), refreshOperation );
	}

	@Override
	protected MigPanel createView() {
		synchronized( this.progressDial ) {
			MigPanel view = new MigPanel( this, "fill", "[grow,right]10[right]10[right]10", "0[]0" );
			view.addComponent( this.errorMessageLabel );
			view.addComponent( this.progressDial, "hidemode 2" );
			view.addComponent( this.refreshOperation.createHyperlink() );
			return view;
		}

	}
}
